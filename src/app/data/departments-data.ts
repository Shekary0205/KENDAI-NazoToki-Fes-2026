export interface StageData {
  id: number;
  location: string;
  riddle: string;
  hint: string;
  answer: string;
  nextLocationHint: string;
  /** チェックリスト形式の場合 */
  type?: "text" | "checkbox";
  /** チェックリストの選択肢 */
  options?: string[];
  /** 正解の選択肢インデックス */
  correctIndices?: number[];
  /** 正解後の解説 */
  explanation?: string;
  /** 次の目的地の補足説明（タイトルと本文） */
  nextLocationDetail?: {
    title: string;
    body: string;
  };
  /** true の場合、次の目的地画面をスキップして直接次の問題へ進む */
  skipNextLocationScreen?: boolean;
}

export interface MidBattleQuestion {
  question: string;
  options: string[];
  type?: "single" | "checkbox";
  correctIndex?: number;
  correctIndices?: number[];
  explanation?: string;
}

export interface MidBattleData {
  /** このバトルを示すID */
  id: number;
  /** このステージIDをクリアした後に発生 */
  afterStageId: number;
  /** 勝利後に進むステージID（未指定の場合は afterStageId + 1） */
  nextStageId?: number;
  enemyName: string;
  enemyImage: string;
  enemyMaxHp: number;
  playerMaxHp: number;
  damageToEnemy: number;
  damageToPlayer: number;
  questions: MidBattleQuestion[];
  /** true の場合、問題プールからランダムに出題し、一度出た問題は繰り返さない */
  randomOrder?: boolean;
  /** 勝利画面で「次の目的地」として表示する案内テキスト */
  nextLocationHint?: string;
}

